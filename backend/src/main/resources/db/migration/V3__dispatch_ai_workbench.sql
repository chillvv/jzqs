CREATE TABLE dispatch_ai_job_logs (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    trigger_source VARCHAR(32) NOT NULL COMMENT 'AUTO_SCHEDULE / MANUAL_RUN / BALANCE_REFRESH',
    serve_date DATE NULL,
    meal_period VARCHAR(16) NULL,
    area_code VARCHAR(64) NULL,
    suggestion_id BIGINT NULL,
    status VARCHAR(16) NOT NULL COMMENT 'SUCCESS / FAILED / SKIPPED',
    suggestion_source VARCHAR(32) NULL,
    reason_summary VARCHAR(255) NOT NULL DEFAULT '',
    message VARCHAR(255) NOT NULL DEFAULT '',
    metadata_json TEXT NULL,
    executed_by VARCHAR(64) NOT NULL DEFAULT 'system',
    started_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    finished_at TIMESTAMP NULL,
    INDEX idx_dispatch_ai_job_logs_started_at (started_at),
    INDEX idx_dispatch_ai_job_logs_scope (serve_date, meal_period, area_code),
    INDEX idx_dispatch_ai_job_logs_trigger (trigger_source, status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

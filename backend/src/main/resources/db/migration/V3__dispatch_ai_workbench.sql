CREATE TABLE dispatch_ai_settings (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    auto_schedule_enabled BIT NOT NULL DEFAULT b'0',
    auto_schedule_time VARCHAR(5) NOT NULL DEFAULT '00:05',
    default_strategy_mode VARCHAR(32) NOT NULL DEFAULT 'NEAR_TO_FAR',
    anchor_name VARCHAR(128) NOT NULL DEFAULT '五环天地',
    anchor_address VARCHAR(255) NOT NULL DEFAULT '湖北省武汉市硚口区荟聚中心',
    ai_enabled BIT NOT NULL DEFAULT b'0',
    api_base_url VARCHAR(255) NOT NULL DEFAULT 'https://api.deepseek.com',
    api_key VARCHAR(255) NOT NULL DEFAULT '',
    ai_model VARCHAR(64) NOT NULL DEFAULT 'deepseek-chat',
    ai_prompt_template TEXT NOT NULL,
    low_balance_threshold DECIMAL(10, 2) NOT NULL DEFAULT 20.00,
    balance_available BIT NOT NULL DEFAULT b'0',
    balance_currency VARCHAR(16) NOT NULL DEFAULT 'CNY',
    total_balance VARCHAR(32) NOT NULL DEFAULT '',
    granted_balance VARCHAR(32) NOT NULL DEFAULT '',
    topped_up_balance VARCHAR(32) NOT NULL DEFAULT '',
    balance_checked_at TIMESTAMP NULL,
    updated_by VARCHAR(64) NOT NULL DEFAULT 'system',
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

